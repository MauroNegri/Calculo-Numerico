# (50 + 15 + 25 + 10)i2 - 50i1 - 10i3 - 25i4 = 0
# (30 + 10 + 20 + 5)i3 - 30i1 - 10i2 - 20i4 = 0
# (10 + 30 + 15 + 20 + 25)i4 - 25i2 - 20i3 = 0

# 80i1 - 50i2 -30i3 - 0i4 = 120
# -50i1 + 100i2 - 10i3 - 25i4 = 0
# -30i1 -10i2 + 65i3 - 20i4 = 0
# 0i1 - 25i2 -20i3 + 100i4 = 0

A = [80 -50 -30   0;
    -50 100 -10 -25;
    -30 -10  65 -20;
      0 -25 -20 100];
b = [120 0 0 0]';

[x] = gauss(A,b)

t = linspace(0, 10, 4);
plot(t, x);
xlabel('t'); ylabel('x(t)');
title('Solución del sistema');
